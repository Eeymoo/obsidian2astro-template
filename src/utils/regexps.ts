/**
 * 正则表达式匹配 Markdown 行内链接：[链接文本](链接地址 "可选标题")
 * 分组说明：
 * 1. ([^\]]+) - 匹配链接文本（除了 ] 之外的所有字符）
 * 2. ([^)"\s]+) - 匹配原始链接地址（除了 )、"、空白符之外的所有字符，避免匹配标题部分）
 * 3. (\s*"[^"]*")? - 匹配可选的链接标题（可选空白符+双引号+标题内容+双引号，? 表示可选）
 */
export const MD_LINK_REG = /\[([^\]]+)\]\(([^)"\s]+)(\s*"[^"]*")?\)/g;

/**
 * 正则表达式匹配 HTML a 标签
 * 用于提取 HTML 中所有的 <a> 标签进行链接处理
 */
export const HTML_A_TAG_REG = /<a[^>]*href=["']([^"']*)["'][^>]*>/gi;
